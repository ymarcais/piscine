#include <unistd.h>

int	newstring(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i + 1] == '\0' || i % 2 == 0)
			&& (str[i] < '0' || str[i] > '9'))
		{
			write(1, "Error\n", 6);
			return (0);
		}
		if (i % 2 == 1 && str[i] != ' ')
		{	
			write(1, "Error\n", 6);
			return (0);
		}
		i++;
	}
	return (1);
}
